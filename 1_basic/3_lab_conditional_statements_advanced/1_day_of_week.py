number_day_entry = int(input())

if number_day_entry == 1:
    print("Monday")
elif number_day_entry == 2:
    print("Tuesday")
elif number_day_entry == 3:
    print("Wednesday")
elif number_day_entry == 4:
    print("Thursday")
elif number_day_entry == 5:
    print("Friday")
elif number_day_entry == 6:
    print("Saturday")
elif number_day_entry == 7:
    print("Sunday")
else:
    print("Error")
